"""Statistical / structural comparison of component generation: `architectures.py` vs `reservoirpy`.

Addresses Round-2 Item 1 of the performance-gap investigation
(``reservoirpy_research/lyapunov/instructions/26_6_5_performance.md``):

Given the Mackey-Glass benchmark hyperparameters, verify that w_in, bias, and w_res
generated by ``architectures.basic_res / basic_in`` and by ``reservoirpy``'s Reservoir node
are statistically equivalent (spectral radius, nonzero count, value bounds, distribution
uniformity).  The two libraries use different RNG schemes so matrices are **not** element-wise
equal; this test is a structural comparison only.

Usage (from repo root, reservoir venv active)::

    python -m reservoirpy.tests.lyapunov.component_stats
"""

